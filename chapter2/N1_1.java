package chapter2;
public class N1_1 {
	public static void main(String[] args) {
		byte a = 10;		//声明一个字节型变量 a，初始化为 10
		short b = 20;		//声明一个短整型变量 b，初始化为 20
		int c = 30;			//声明一个整型变量 c，初始化为 30
		long d = 40;		//声明一个长整型变量 d，初始化为 40
		float e = 5.0f;		//声明一个单精度型变量 e，初始化为 5.0
		double f = 6.0;		//声明一个双精度型变量 f，初始化为 6.0
		char g = 'A';		//声明一个字符型变量 g，初始化为‘A’
		boolean h=true;		//声明一个布尔型变量 h，初始化为 true
		System.out.println("字节型变量		a="+a);
		System.out.println("短整型变量		b="+b);
		System.out.println("整型变量		c="+c);
		System.out.println("长整型变量 	d="+d);
		System.out.println("单精度型变量 	e="+e);
		System.out.println("双精度型变量 	f="+f);
		System.out.println("字符型变量 	g="+g);
		System.out.println("布尔型变量 	h="+h);
		}
}